#include "bits/stdc++.h"
using namespace std;
// compile : g++ -std=c++17 hogehoge.cpp


int main()
{
    cout << 1+1 << endl;
    cout << 3-4 << endl;
    cout << 2*3 << endl;
    cout << 7/3 << endl;
    // 小数点を切り捨てない時は以下のように書く．
    cout << 7.0/3.0 << endl;
    cout << 7.5/3.5 << endl;
    cout << 7%3 << endl;
    cout << 4%5 << endl;
    cout << 1 + 2*3 << endl;
    cout << (1+2)*3 << endl;
    cout << 3/2*4 << endl;
    cout << 3*4/2 << endl;
    cout << 3/0 << endl;
}